def _viewport_size(data: dict[str, Any]) -> tuple[Any, Any] | None:
    if data.get("width") is not None and data.get("height") is not None:
        return data.get("width"), data.get("height")
    for outer_name in ("current", "viewport", "info"):
        outer = data.get(outer_name)
        if isinstance(outer, dict):
            for size_name in ("window_size", "size", "visible_rect"):
                size = outer.get(size_name)
                if isinstance(size, dict) and size.get("width") is not None and size.get("height") is not None:
                    return size.get("width"), size.get("height")
    return None


