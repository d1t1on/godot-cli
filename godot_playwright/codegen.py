def _safe_test_name(name: str) -> str:
    normalized = re.sub(r"\W+", "_", name.strip())
    normalized = normalized.strip("_") or "generated_trace"
    if not normalized.startswith("test_"):
        normalized = f"test_{normalized}"
    if normalized[0].isdigit():
        normalized = f"test_{normalized}"
    return normalized


