def _editor_selection_selectors(data: dict[str, Any]) -> Any:
    selectors = data.get("selectors")
    if isinstance(selectors, list) and selectors:
        return selectors[0] if len(selectors) == 1 else selectors
    selector = data.get("selector")
    if selector is not None:
        return selector
    nodes = data.get("nodes")
    if isinstance(nodes, list):
        paths = [node.get("path") for node in nodes if isinstance(node, dict) and node.get("path")]
        if paths:
            return paths[0] if len(paths) == 1 else paths
    return data.get("path", "")


def _editor_target_call(data: dict[str, Any]) -> tuple[list[Any], list[tuple[str, Any]]]:
    args: list[Any] = []
    kwargs: list[tuple[str, Any]] = []
    selector = data.get("selector")
    if selector is not None:
        args.append(selector)
    elif data.get("path"):
        kwargs.append(("path", data.get("path")))
    elif data.get("resource_path"):
        kwargs.append(("resource_path", data.get("resource_path")))
    else:
        target = data.get("target")
        if isinstance(target, dict) and target.get("path"):
            path = target.get("path")
            if isinstance(path, str) and path.startswith("res://"):
                kwargs.append(("resource_path", path))
            else:
                kwargs.append(("path", path))
    kwargs.extend(_root_kwargs(data))
    return args, kwargs


