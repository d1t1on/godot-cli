class TraceCodegenError(ValueError):
    pass


_INFRASTRUCTURE_EVENTS = {"rpc", "trace.start", "trace.stop"}


def load_trace_events(path: str | Path) -> list[dict[str, Any]]:
    trace_path = Path(path).expanduser().resolve()
    try:
        raw = json.loads(trace_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise TraceCodegenError(f"Trace JSON is invalid: {trace_path}: {exc}") from exc
    return normalize_trace_events(raw)


def normalize_trace_events(trace: Any) -> list[dict[str, Any]]:
    if isinstance(trace, list):
        raw_events = trace
    elif isinstance(trace, dict) and isinstance(trace.get("events"), list):
        raw_events = trace["events"]
    else:
        raise TraceCodegenError("Trace input must be a JSON event array or an object with an events array")

    events: list[dict[str, Any]] = []
    for index, raw_event in enumerate(raw_events):
        if not isinstance(raw_event, dict):
            raise TraceCodegenError(f"Trace event {index} must be an object")
        event_type = raw_event.get("type")
        if not event_type:
            raise TraceCodegenError(f"Trace event {index} is missing type")
        data = raw_event.get("data", {})
        if data is None:
            data = {}
        if not isinstance(data, dict):
            raise TraceCodegenError(f"Trace event {index} data must be an object")
        events.append(dict(raw_event, type=str(event_type), data=data))
    return events


def generate_test_from_trace(
    trace: Any,
    *,
    test_name: str = "test_generated_trace",
    include_header: bool = True,
) -> str:
    events = normalize_trace_events(trace)
    function_name = _safe_test_name(test_name)
    body: list[str] = [
        "# Generated by godot-playwright codegen-trace.",
        "# Review selectors and add assertions before committing.",
    ]

    index = 0
    generated_count = 0
    unsupported_count = 0
    while index < len(events):
        event = events[index]
        lines, consumed = _event_lines(events, index)
        if lines:
            body.extend(lines)
            if not all(line.lstrip().startswith("#") for line in lines):
                generated_count += 1
            else:
                unsupported_count += 1
        index += max(consumed, 1)

    if generated_count == 0 and unsupported_count == 0:
        body.append("pass")

    lines: list[str] = []
    if include_header:
        lines.extend(["from __future__ import annotations", "", ""])
    lines.append(f"def {function_name}(godot):")
    lines.extend(f"    {line}" for line in body)
    lines.append("")
    return "\n".join(lines)


def _event_lines(events: list[dict[str, Any]], index: int) -> tuple[list[str], int]:
    event = events[index]
    event_type = str(event.get("type", ""))
    data = _event_data(event)
    if event_type in _INFRASTRUCTURE_EVENTS:
        return [], 1

    if event_type == "input.key":
        paired = _paired_key_press(events, index)
        if paired is not None:
            return [paired], 2

    line = _single_event_line(event_type, data)
    if line is None:
        return [_unsupported_comment(event_type, data)], 1
    return [line], 1


