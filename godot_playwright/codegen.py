def _paired_key_press(events: list[dict[str, Any]], index: int) -> str | None:
    first = events[index]
    if not bool(_event_data(first).get("pressed", True)):
        return None
    if index + 1 >= len(events):
        return None
    second = events[index + 1]
    if second.get("type") != "input.key":
        return None
    first_data = _event_data(first)
    second_data = _event_data(second)
    if bool(second_data.get("pressed", True)):
        return None
    if first_data.get("key") != second_data.get("key"):
        return None
    if first_data.get("modifiers", {}) != second_data.get("modifiers", {}):
        return None
    return f"godot.press({_py(first_data.get('key', first_data.get('resolved_key', '')))}{_modifier_kwargs(first_data)})"


def _joypad_line(data: dict[str, Any]) -> str:
    joypad_type = str(data.get("type", "button"))
    if joypad_type == "axis":
        return f"godot.joypad_axis({_py(data.get('axis', data.get('axis_index', 0)))}, {_py(data.get('value', 0.0))}, device={_py(data.get('device', 0))})"
    button = data.get("button", data.get("button_index", 0))
    if bool(data.get("pressed", True)):
        pressure = data.get("pressure")
        pressure_arg = "" if pressure in (None, 1, 1.0) else f", pressure={_py(pressure)}"
        return f"godot.joypad_button_down({_py(button)}{pressure_arg}, device={_py(data.get('device', 0))})"
    return f"godot.joypad_button_up({_py(button)}, device={_py(data.get('device', 0))})"


def _mouse_line(data: dict[str, Any]) -> str:
    x = data.get("x", 0.0)
    y = data.get("y", 0.0)
    mouse_type = str(data.get("type", "button"))
    if mouse_type == "move":
        button_mask = data.get("button_mask", 0)
        suffix = "" if not button_mask else f", button_mask={_py(button_mask)}"
        return f"godot.mouse_move({_py(x)}, {_py(y)}{suffix})"
    if mouse_type == "down":
        return f"godot.mouse_down({_py(x)}, {_py(y)}, button={_py(data.get('button', 1))})"
    if mouse_type == "up":
        return f"godot.mouse_up({_py(x)}, {_py(y)}, button={_py(data.get('button', 1))})"
    if mouse_type == "click":
        return f"godot.mouse_click({_py(x)}, {_py(y)}, button={_py(data.get('button', 1))})"
    if mouse_type == "wheel":
        return f"godot.mouse_wheel({_py(x)}, {_py(y)}, delta_x={_py(data.get('delta_x', 0.0))}, delta_y={_py(data.get('delta_y', 0.0))})"
    return f"godot.input_mouse({_py(x)}, {_py(y)}, type={_py(mouse_type)})"


def _touch_line(data: dict[str, Any]) -> str:
    x = data.get("x", 0.0)
    y = data.get("y", 0.0)
    index = data.get("index", 0)
    touch_type = str(data.get("type", "tap"))
    if touch_type == "tap":
        return f"godot.tap({_py(x)}, {_py(y)}, index={_py(index)})"
    if touch_type == "down":
        return f"godot.touch_down({_py(x)}, {_py(y)}, index={_py(index)})"
    if touch_type == "up":
        return f"godot.touch_up({_py(x)}, {_py(y)}, index={_py(index)})"
    if touch_type in {"drag", "move"}:
        return f"godot.touch_move({_py(x)}, {_py(y)}, index={_py(index)})"
    return f"godot.input_touch({_py(x)}, {_py(y)}, type={_py(touch_type)}, index={_py(index)})"


